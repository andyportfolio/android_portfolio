public class ListViewMyResponseItem {


    private String regtime;
    private String regcompany;
    private String content;
    private String replycnt;



    private String response_rnum;

    public ListViewMyResponseItem() {
    }

    public String getRegtime() {
        return regtime;
    }

    public void setRegtime(String regtime) {
        this.regtime = regtime;
    }

    public String getRegcompany() {
        return regcompany;
    }

    public void setRegcompany(String regcompany) {
        this.regcompany = regcompany;
    }

    public String getContent() {
        return content;
    }

    public void setContent(String content) {
        this.content = content;
    }

    public String getReplycnt() {
        return replycnt;
    }

    public void setReplycnt(String replycnt) {
        this.replycnt = replycnt;
    }

    public String getResponse_rnum() {
        return response_rnum;
    }

    public void setResponse_rnum(String response_rnum) {
        this.response_rnum = response_rnum;
    }

}
